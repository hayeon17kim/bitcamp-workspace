// 관계연산자(relational operators: <, <=, >, >=)

public class Exam0210 {
  public static void main(String[] args) {
    int a = 10;
    int b = 20;
    
    boolean r1 = a < b;
    
    //int r2 = a < b;
    
    System.out.println(a < b);
    System.out.println(a <= b);
    System.out.println(a > b);
    System.out.println(a >= b);
    System.out.println(a == b);
    System.out.println(a != b);
  }
}
