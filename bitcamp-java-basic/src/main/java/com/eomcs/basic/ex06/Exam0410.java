package com.eomcs.basic.ex06;

//# 흐름 제어문 - for 반복문
public class Exam0410 {
  public static void main(String[] args) {
    for (int i = 1; i < 5; i++)
      System.out.println(i);

    // 실행 순서
    // 1) 변수 초기화
    // 2) 조건
    // 3) 문장
    // 4) 변수 증가문
    // 조건이 참인 동안 2~4를 반복한다.

    // for 문에서 선언한 변수는 그 for 문 안에서만 사용할 수 있다.
  }
}
